    private void makeRequest(File file) {
        poslednaslikaime = file.getName();
        Bitmap bitmap = decodeFile(file);
        ByteArrayOutputStream stream = new ByteArrayOutputStream();
        bitmap.compress(Bitmap.CompressFormat.JPEG, 70, stream);
        bitmap.recycle();
        byte[] array = stream.toByteArray();
        encoded_string = Base64.encodeToString(array, Base64.DEFAULT);
        RequestQueue requestQueue = Volley.newRequestQueue(MainActivity.this);
        StringRequest request = new StringRequest(
                Request.Method.POST,
                MainActivity.UPLOAD_URL,
                new Response.Listener<String>() {
                    @Override
                    public void onResponse(String response) {
                            responsesArray.add(0, response);
                            adapter.notifyDataSetChanged();

                        //TUKA PRAKJAM BLUETOOTH PORAKA
                        try {
                            outputStream.write(response.getBytes());
                        } catch (IOException e) {
                            e.printStackTrace();
                        }

                        Toast.makeText(MainActivity.this,response,Toast.LENGTH_LONG).show();
                    }
                }, new Response.ErrorListener(){
            @Override
            public void onErrorResponse(VolleyError error) {
                Toast.makeText(MainActivity.this, error.getMessage(), Toast.LENGTH_LONG).show();
            }
        }){
        @Override
        protected Map<String, String> getParams() throws AuthFailureError {
            HashMap<String, String> map = new HashMap<>();
            map.put("encoded_string", encoded_string);
            map.put("image_name", poslednaslikaime);
            map.put("user_name", "default");

            return map;
    }
        };
        requestQueue.add(request);
}


